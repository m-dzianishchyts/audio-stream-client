package by.bsuir.audio_stream.client.util;

public class UrlBuildingException extends Exception {

    private static final long serialVersionUID = 8031579283692758066L;

    public UrlBuildingException() {
    }

    public UrlBuildingException(String message) {
        super(message);
    }

    public UrlBuildingException(String message, Throwable cause) {
        super(message, cause);
    }

    public UrlBuildingException(Throwable cause) {
        super(cause);
    }
}
